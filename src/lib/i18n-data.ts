export function getTranslations(locale: Locale): Record<string, string> {
  return translations[locale] ?? translations.en;
}

export function resolveLocale(nativeLang?: string | null): Locale {
  if (!nativeLang) return "en";
  const lower = nativeLang.toLowerCase();
  if (lower === "pl" || lower === "polski") return "pl";
  return "en";
}

export function detectBrowserLocale(): Locale {
  if (typeof window !== "undefined") {
    try {
      const stored = localStorage.getItem("godoj_ui_locale");
      if (stored === "pl" || stored === "en") return stored;
    } catch {}
    const lang = navigator.language?.toLowerCase() ?? "";
    if (lang.startsWith("pl")) return "pl";
  }
  return "en";
}
